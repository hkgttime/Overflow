package org.oj.entity;

public class IOExample {

    private String in;
    private String out;

    public IOExample() {
    }

    public IOExample(String in, String out) {
        this.in = in;
        this.out = out;
    }

    public String getIn() {
        return in;
    }

    public String getOut() {
        return out;
    }

    public void setIn(String in) {
        this.in = in;
    }

    public void setOut(String out) {
        this.out = out;
    }
}
